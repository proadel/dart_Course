// switch-case statements
// switch (var){ case ... {codes... + break; } -->
// default: {codes} 
void main(){
var number = 20;
 switch (number) {
  case 201: 
  {
    print ('true');
    break; // to STOP codes if first case = condition or --> default
  }
  case 20:{
    print ('true 2');
    break;
  }
  default: 
  {
      print ('false');
    }
 }
}